    // テーブルの内容を更新するメソッド
    private void tableUpdate() {
        try (Connection conn1 = getConnection()) {
            // Productテーブルから全てのデータを取得するSQLクエリを準備
            pst = conn1.prepareStatement(""
                    + "SELECT Product.id, Product.barcode, Product.product, Category.category, Product.price, Product.quantity, Category.status "
                    + "FROM Product "
                    + "INNER JOIN Category "
                    + "ON Product.category_id = Category.category_id "
                    + "ORDER BY Product.id;");
            ResultSet rs = pst.executeQuery();
            // テーブルモデルを取得し、行数をリセット
            DefaultTableModel d = (DefaultTableModel) jTable1.getModel();
            d.setRowCount(0);
            // クエリの結果セットを反復処理してテーブルに追加
            while (rs.next()) {
                ArrayList<String> row = new ArrayList<>();

                row.add(rs.getString("id"));
                row.add(rs.getString("barcode"));
                row.add(rs.getString("product"));
                row.add(rs.getString("category"));
                row.add(rs.getString("price"));
                row.add(rs.getString("quantity"));
                row.add(rs.getString("status"));
                d.addRow(row.toArray());
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

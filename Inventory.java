    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // 検索ボタンのアクションハンドラー
        String keyword = txtSearch.getText().trim();
        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(null, "検索キーワードを入力してください。");
        }

        ResultSet rs = null;

        try (Connection conn1 = getConnection()) {

            // 検索用のSQL文を準備
            String sql = ""
                    + "SELECT Product.id, Product.barcode, Product.product, Category.category, Product.price, Product.quantity, Category.status "
                    + "FROM Product "
                    + "INNER JOIN Category "
                    + "ON Product.category_id = Category.category_id "
                    + "WHERE Product.product LIKE ? "
                    + "ORDER BY Product.id; ";
            pst = conn1.prepareStatement(sql);
            pst.setString(1, "%" + keyword + "%");
            rs = pst.executeQuery();

            // テーブルモデルを取得してデータをクリア
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            // 検索結果をテーブルモデルに追加
            boolean hasResults = false;
            while (rs.next()) {
                int id = rs.getInt("id");
                int barcode = rs.getInt("barcode");
                String product = rs.getString("product");
                String category = rs.getString("category");
                int price = rs.getInt("price");
                int quantity = rs.getInt("quantity");
                String status = rs.getString("status");

                model.addRow(new Object[]{id, barcode, product, category, price, quantity, status});
            }
            // 検索結果がない場合、メッセージを表示
            if (!hasResults) {
                JOptionPane.showMessageDialog(null, "該当する商品が見つかりませんでした。");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "データベースエラーが発生しました。");
        } finally {
            // データベースリソースを閉じる
            try {
                if (rs != null) {
                    rs.close();
                }
                if (pst != null) {
                    pst.close();
                }
                if (conn1 != null) {
                    conn1.close();
                }
            } catch (SQLException ex) {
                Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

